package T1BasicPrograms;

public class P4DataTypes {
	public static void main(String[] args) {
		//Primitive data types
		//Integer
		//byte,short,int,long
		byte age = 25; //-128 to 127 - 1B
		short studentCount = 1000; //-32k to 32k -2B
		int balance = 2_000_000; // -2B to 2B -4B
		long phoneNumber = 20350719590l; //8B
		
		//Decimal Values
		float percentage = 80.6F; //4b
		double cgpa = 9.2;
		
		//Character 
		char c = 'a';
		
		//String
		String name = "Akhil";
		
		//Boolean
		boolean isValid = true; //1b
		
		//Reference Data Types
		//To store complex objects
		//ClassName referenceVariable = new ClassName(); - To create an object to a class
		String course = new String("Java");
		
		
		
		
		
	}

}
